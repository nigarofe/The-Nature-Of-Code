function draw() {
    let smallestError = Infinity;
    bestIndex = -1;
    let errors = new Array(nets.length);
    for (let i = 0; i < nets.length; i++) {
        errors[i] = calculateNetworkError(nets[i]);
        if (errors[i] < smallestError) {
            smallestError = errors[i];
            bestIndex = i;
        }
    }

    for (let i = 0; i < nets.length; i++) {
        if (i != bestIndex) {
            // nets[i] = Network.getMutation(nets[bestIndex]);
            nets[i] = Network.getMutation(nets[bestIndex]);
        }
    }

    if (frameCount % 1 == 0) {
        background(0);
        drawAnswer();
        networkDraw(nets[bestIndex]);
        showFPS();
        showMouseLocation();
    }
}

function normAnswer() {
    for (let i = 0; i < answer.length; i++) {
        answer[i] = map(answer[i], 0, 60, 400, 100)
    }
}

function predict(size) {
    for (let i = originalLength; i < originalLength + size; i++) {
        answer[i] = nets[bestIndex].calculate([i, answer[i - 1]]);
    }
}

function calculateNetworkError(network) {
    let error = 0;

    for (let x = 1; x < originalLength; x++) {
        let y = network.calculate([x, answer[x - 1]]);
        if (answer[x] - y < 0) {
            error += -1 * (answer[x] - y);
        } else {
            error += answer[x] - y;
        }

    }
    return error;
}

function networkDraw(network) {
    stroke(255, 255, 255);
    strokeWeight(1);
    noFill();

    beginShape();
    for (let x = 1; x < originalLength; x++) {
        vertex(x, network.calculate([x, answer[x - 1]]));
    }
    endShape();
}

